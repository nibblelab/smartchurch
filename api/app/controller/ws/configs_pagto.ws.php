<?php
require_once ADO_PATH . '/ConfigsPagto.class.php'; 
require_once DAO_PATH . '/ConfigsPagtoDAO.class.php'; 

/**
 * API REST de ConfigsPagto
 */
class ConfigsPagtoWS extends WSUtil
{
    /**
     * 
     * @var \ConfigsPagtoWS singleton instance
     */
    private static $_Instance = null;
    
    /**
     * Get singleton instance
     * 
     * @return \ConfigsPagtoWS
     */
    public static function getInstance(): \ConfigsPagtoWS {
        if(self::$_Instance == null) {
            self::$_Instance = new self();
        }
        return self::$_Instance;
    }
    
    /**
     * 
     * Cria
     * 
     * @httpmethod POST
     * @auth yes
     * @return array
     */
    public function create(): array
    {
        if(!doIHavePermission(NblFram::$context->token, '')) {
            return array('status' => 'no', 'success' => false, 'msg' => 'Você não tem permissão para executar essa operação');
        }
        
        $dto = new ConfigsPagtoDTO();
        $obj = new ConfigsPagtoADO();
        
        $dto->add = true;
        $dto->id = NblFram::$context->data->id;
        $dto->nome = NblFram::$context->data->nome;
        $dto->email = NblFram::$context->data->email;
        $dto->token_prod = NblFram::$context->data->token_prod;
        $dto->token_test = NblFram::$context->data->token_test;
        $dto->ref = NblFram::$context->data->ref;
        $dto->ref_tp = NblFram::$context->data->ref_tp;
        
        $obj->add($dto);
        if($obj->sync())
        {
            return array('status' => 'ok', 'success' => true, 'id' => $dto->id);
        }
        else
        {
            return array('status' => 'no', 'success' => false, 'msg' => 'Erro na operação', 'errs' => $obj->getErrs());
        }
    }

